# Data Structure Operations:
# Read: Reading refers to looking something up at a particular spot within
# the data structure.
# Search: Searching refers to looking for a particular value within a data
# structure.
# Insert: Insertion refers to adding a new value to our data structure.
# Delete: Deletion refers to removing a value from our data structure.

